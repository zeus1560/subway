// 두 좌표 간 거리 계산 (Haversine 공식, km 단위)
export function calculateDistance(
  lat1: number,
  lon1: number,
  lat2: number,
  lon2: number
): number {
  const R = 6371; // 지구 반지름 (km)
  const dLat = (lat2 - lat1) * (Math.PI / 180);
  const dLon = (lon2 - lon1) * (Math.PI / 180);
  const a =
    Math.sin(dLat / 2) * Math.sin(dLat / 2) +
    Math.cos(lat1 * (Math.PI / 180)) *
      Math.cos(lat2 * (Math.PI / 180)) *
      Math.sin(dLon / 2) *
      Math.sin(dLon / 2);
  const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));
  return R * c;
}

// 사용자 위치 기반 가까운 역 찾기
export function findNearbyStations(
  userLat: number,
  userLon: number,
  radiusKm: number = 1.0
): Array<StationCoordinate & { distance: number }> {
  const nearby: Array<StationCoordinate & { distance: number }> = [];
  
  STATION_COORDINATES.forEach((station) => {
    const distance = calculateDistance(
      userLat,
      userLon,
      station.latitude,
      station.longitude
    );
    
    if (distance <= radiusKm) {
      nearby.push({
        ...station,
        distance: Math.round(distance * 1000) / 1000, // 소수점 3자리까지
      });
    }
  });
  
  // 거리순 정렬
  return nearby.sort((a, b) => a.distance - b.distance);
}

